window.selectLang = function(lang) {
  setLanguage(lang);
  window.removeEventListener('keydown', window.handleLangKey);
  
  const gate = document.getElementById('lang-gate');
  if (gate && typeof gsap !== 'undefined') {
    gsap.to(gate, {
      opacity: 0,
      y: -60,
      scale: 0.95,
      duration: 0.6,
      ease: 'power3.inOut',
      onComplete: () => gate.remove()
    });
  } else if (gate) {
    gate.remove();
  }
};

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
  initLanguageGate();
});
